CREATE DATABASE aliens;
USE aliens;

CREATE TABLE raw_input (
 alien_id VARCHAR(40), alien_name VARCHAR(40),
 alien_type VARCHAR(40), alien_color VARCHAR(40),
 witness_id VARCHAR(40), witness_name VARCHAR(40),
 witness_last_name VARCHAR(40), witness_address VARCHAR(40),
 witness_age VARCHAR(40), location_id VARCHAR(40),
 lat VARCHAR(40),lon VARCHAR(40),
 place VARCHAR(40), country VARCHAR(40),
 region VARCHAR(40), time_of_day VARCHAR(40)
);